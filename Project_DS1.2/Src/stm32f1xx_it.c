extern uint8_t RX;
void Yunyin_GPIOinput(void)
{
	if(HAL_GPIO_ReadPin(GPIOB,GPIO_PIN_14) == RESET) 
		RX = 'e';//复位
	else if(HAL_GPIO_ReadPin(GPIOB,GPIO_PIN_12) == RESET) 
		RX = 'c';//左转
	else if(HAL_GPIO_ReadPin(GPIOB,GPIO_PIN_13) == RESET) 
		RX = 'd';//右转
	else if(HAL_GPIO_ReadPin(GPIOB,GPIO_PIN_15) == RESET) 
		RX = 'f';//起立
}

